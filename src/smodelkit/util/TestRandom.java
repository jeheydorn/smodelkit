	public double nextGaussian()
	{
		gaussIndex += 1;
		if (gaussIndex >= gaussSamples.length)
			gaussIndex = 0;
		//out.println("gauss sample: " + gaussSamples[gaussIndex]);
		return gaussSamples[gaussIndex];
	}

	public int nextInt()
	{
		intIndex += 1;
		if (intIndex >= intSamples.length)
			intIndex = 0;
		//out.println("int sample: " + intSamples[intIndex]);
		return intSamples[intIndex];
	}

	public int nextInt(int range)
	{
		int sample = Math.abs(nextInt());
		return sample % range;
	}

		
